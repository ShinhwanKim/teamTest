package com.example.jsych.teamchicken;

public class Seat {
    int no;
    int check;

    public Seat(int no, int check) {
        this.no = no;
        this.check = check;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public int getCheck() {
        return check;
    }

    public void setCheck(int check) {
        this.check = check;
    }
}
